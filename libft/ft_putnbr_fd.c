#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == INT_MIN)
	{
		ft_putnbr_fd(-(INT_MAX / 10), fd);
		ft_putnbr_fd((INT_MAX % 10) + 1, fd);
	}
	else
	{
		if (n < 0)
		{
			write(fd, &"-", 1);
			n *= -1;
		}
		if (n >= 10)
			ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd(n % 10 + '0', fd);
	}
}
