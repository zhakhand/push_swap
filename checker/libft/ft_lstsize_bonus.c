#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	sz;

	sz = 0;
	if (lst)
	{
		sz++;
		while (lst->next)
		{
			lst = lst->next;
			sz++;
		}
	}
	return (sz);
}
